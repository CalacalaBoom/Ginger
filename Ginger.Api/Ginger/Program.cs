using $safeprojectname$.Core;
using Microsoft.OpenApi.Models;
using SqlSugar;

namespace $safeprojectname$
{
    /// <summary>
    /// 作者:$username$
    /// 时间:$time$
    /// 机器名:$machinename$
    /// 项目名:$projectname$
    /// </summary>
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            //注册Swagger生成器，定义一个和多个Swagger 文档
            builder.Services.AddSwaggerGen(options =>
            {
                options.SwaggerDoc("v1", new OpenApiInfo
                {
                    Version = "v1", 
                    Title = "$projectname$ API文档",
                    Description = "by 张宇,tel:15250790091",
                    TermsOfService = new Uri("https://github.com/CalacalaBoom"),
                    Contact = new OpenApiContact
                    {
                        Name = "Example Contact",
                        Url = new Uri("https://github.com/CalacalaBoom")
                    },
                    License = new OpenApiLicense
                    {
                        Name = "Example License",
                        Url = new Uri("https://github.com/CalacalaBoom")
                    }
                });
            });
            //注入仓储
            builder.Services.AddScoped(typeof(Repository<>));
            //注册上下文：AOP里面可以获取IOC对象，如果有现成框架比如Furion可以不写这一行
            builder.Services.AddHttpContextAccessor();
            //注册SqlSugar
            builder.Services.AddSingleton<ISqlSugarClient>(s =>
            {
                SqlSugarScope sqlSugar = new SqlSugarScope(new ConnectionConfig()
                {
                    DbType = SqlSugar.DbType.Sqlite,
                    ConnectionString = $"DataSource=./test.db",
                    IsAutoCloseConnection = true,
                },
               db =>
               {
                   //每次上下文都会执行

                   //获取IOC对象不要求在一个上下文
                   //vra log=s.GetService<Log>()

                   //获取IOC对象要求在一个上下文
                   //var appServive = s.GetService<IHttpContextAccessor>();
                   //var log= appServive?.HttpContext?.RequestServices.GetService<Log>();

                   db.Aop.OnLogExecuting = (sql, pars) =>
                   {
                       Console.WriteLine(sql);//输出sql,查看执行sql 性能无影响
                   };
               });
                return sqlSugar;
            });
            var app = builder.Build();

            // Configure the HTTP request pipeline.
            //if (app.Environment.IsDevelopment())
            //{
                app.UseSwagger();
                app.UseSwaggerUI(c =>
                {
                    c.RoutePrefix = "";
                    c.SwaggerEndpoint("/swagger/v1/swagger.json", "V1 Docs");

                });
            //}

            app.UseAuthorization();

            app.MapControllers();

            app.Run();
        }
    }
}
